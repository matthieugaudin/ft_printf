#include "../ft_printf.h"

int	ft_print_s(char *s)
{
	int	s_len;

	s_len = ft_strlen(s);
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
    else
	{
		write(1, s, s_len);
		return (s_len);
	}
}
